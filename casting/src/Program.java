public class Program {

    public static void main(String[] args) {

        int a, b;
        double resultado;

        a = 5;
        b = 2;

        resultado = (double) a / b; // Casting "(double)".

        System.out.println(resultado);

        double i;
        int j;

        i = 5.0;
        j = (int) i; // Casting "(int)".

        System.out.println(j);
    }
}
